# slicing = criate a substring by extracting elements from another string
#  indexing[] or slice()
#  [start:stop:step]

name = "Andrey Yunin"
first_name = name[0:6:1]
last_name = name [7:]
reversed_name = name [:: -1]

print (first_name)
print (last_name)
print (reversed_name)


